async function main() {
    try {
        await displayBanner();

        console.log('\n‚è≥ Starting server...');

        // Start the server
        const { port } = await startServer();
        const localUrl = `http://localhost:${port}`;

        console.log(`‚úì Server started on port ${port}`);

        // Create initial window
        console.log('\n‚è≥ Creating initial Antigravity window...');
        const initialWindow = windowManager.createWindow({ name: 'Main Terminal' });
        console.log(`‚úì Window created: ${initialWindow.name}`);

        // Start tunnel
        let publicUrl = null;
        console.log('\n‚è≥ Connecting to ngrok tunnel...');

        try {
            publicUrl = await tunnelManager.connect(port);
            console.log(`‚úì Tunnel connected!`);
        } catch (error) {
            console.log(`‚úó Tunnel connection failed: ${error.message}`);
            console.log('  Continuing in local-only mode.');
        }

        // Display connection info
        displayConnectionInfo(localUrl, publicUrl);

    } catch (error) {
        console.error(`\n‚úó Launch failed: ${error.message}`);
        console.error(error.stack);
        process.exit(1);
    }
}

// Handle cleanup
process.on('SIGINT', async () => {
    console.log('\n\n‚è≥ Shutting down...');

    try {
        await tunnelManager.disconnect();
        windowManager.destroyAll();
        console.log('‚úì Cleanup complete');
    } catch (error) {
        console.error(`‚úó Cleanup error: ${error.message}`);
    }

    process.exit(0);
});

main();
